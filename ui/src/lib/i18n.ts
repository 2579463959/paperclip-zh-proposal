export function detectInitialLocale(): Locale {
  if (typeof window !== "undefined") {
    const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    if (stored === "en" || stored === "zh-CN") return stored;

    const language = window.navigator.language.toLowerCase();
    if (language.startsWith("zh")) return "zh-CN";
  }

  return "en";
}

export function persistLocale(locale: Locale) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
}

function translateDynamicZh(text: string): string | null {
  const rules: Array<[RegExp, (...parts: string[]) => string]> = [
    [/^Finished\s+(.+)\s+ago$/i, (time) => `${time}前完成`],
    [/^Started\s+(.+)\s+ago$/i, (time) => `${time}前开始`],
    [/^Comments & Runs \((\d+)\)$/i, (count) => `评论与运行（${count}）`],
    [/^(\d+)\s+Agents Enabled$/i, (count) => `${count} 个已启用智能体`],
    [/^(\d+)\s+Tasks In Progress$/i, (count) => `${count} 个进行中的任务`],
    [/^(\d+)\s+Pending Approvals$/i, (count) => `${count} 个待审批项`],
    [/^(\d+)\s+stale tasks$/i, (count) => `${count} 个滞留任务`],
    [/^(\d+)\s+open$/i, (count) => `${count} 个开放`],
    [/^(\d+)\s+blocked$/i, (count) => `${count} 个阻塞`],
    [/^(\d+)\s+errors$/i, (count) => `${count} 个错误`],
    [/^Last\s+(\d+)\s+days$/i, (days) => `最近 ${days} 天`],
    [/^changed status from\s+(.+)\s+to\s+(.+)\s+on$/i, (from, to) => `将状态从 ${translate("zh-CN", from)} 改为 ${translate("zh-CN", to)}`],
    [/^changed status to\s+(.+)\s+on$/i, (to) => `将状态改为 ${translate("zh-CN", to)}`],
    [/^changed priority from\s+(.+)\s+to\s+(.+)\s+on$/i, (from, to) => `将优先级从 ${translate("zh-CN", from)} 改为 ${translate("zh-CN", to)}`],
    [/^changed priority to\s+(.+)\s+on$/i, (to) => `将优先级改为 ${translate("zh-CN", to)}`],
  ];

  for (const [pattern, formatter] of rules) {
    const match = text.match(pattern);
    if (match) {
      return formatter(...match.slice(1));
    }
  }

  return null;
}

export function translate(locale: Locale, text: string, vars?: Record<string, string | number>) {
  const template = messages[locale][text] ?? (locale === "zh-CN" ? (translateDynamicZh(text) ?? text) : text);
  if (!vars) return template;

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = vars[key];
    return value === undefined ? `{${key}}` : String(value);
  });
}
